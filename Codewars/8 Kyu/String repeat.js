//                          String repeat


// Write a function called repeatStr which repeats the given string exactly n times.
// Example: 
// repeatStr(6, "I")       => "IIIIII"
// repeatStr(5, "Hello")   => "HelloHelloHelloHelloHello"

const repeatStr = (n, s) => s.repeat(n);
